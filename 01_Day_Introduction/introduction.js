//DATA TYPES
console.log(typeof "Arzu DEMIR"); //string data type
console.log(typeof 5); //number data type
console.log(typeof true); //boolean data type
console.log(typeof undefined); //undefined data type
console.log(typeof null); //object data type

//VARIABLES
// let = For a variable that changes at a different time, we use let.
let firstName = "Arzu";
let lastName = "Demir";
let country = "Turkey";
let city = "Sanliurfa";
let age = 15;
let isStudent = true;
console.log(firstName, lastName, country, city, age, isStudent );

// const =  If the data does not change at all, we use const.
const gravity = 9.81;
const PI = 3.14;
const boilingPoint = 100;
console.log(gravity, PI, boilingPoint);
